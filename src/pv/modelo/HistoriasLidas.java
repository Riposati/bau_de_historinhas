package pv.modelo;
/**
 *
 * @author Gustavo
 */
public class HistoriasLidas {
    
    private int idHistoria;
    private boolean lidas;

    /**
     * @return the idHistoria
     */
    public int getIdHistoria() {
        return idHistoria;
    }

    /**
     * @param idHistoria the idHistoria to set
     */
    public void setIdHistoria(int idHistoria) {
        this.idHistoria = idHistoria;
    }

    /**
     * @return the lidas
     */
    public boolean isLidas() {
        return lidas;
    }

    /**
     * @param lidas the lidas to set
     */
    public void setLidas(boolean lidas) {
        this.lidas = lidas;
    }
    
}
